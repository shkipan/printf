#include "../libft.h"

int		ft_print_sym(t_format **prop, char c)
{
	if ((*prop)->plus == 1)
		write(1, "+", 1);
	if ((*prop)->minus == 1)
		write(1, "-", 1);
	write(1, &c, 1);
	return (1);
}
